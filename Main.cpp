double computeMean(vector<double> v){
	double s=0;
	for (double d:v)
		s+=d;
	s=s/(double)v.size();
	return s;
}

double computeStdDev(vector<double> v){
	double m=computeMean(v);
	double s=0;
	for (double d:v)
		s+=pow(d-m,2);
	s=s/((double)v.size()-1);
	return sqrt(s);
}

double computeMedian(vector<double>::const_iterator begin,
              vector<double>::const_iterator end) {
    int len = end - begin;
    auto it = begin + len / 2;
    double m = *it;
    if ((len % 2) == 0) m = (m + *(--it)) / 2;
    return m;
}

